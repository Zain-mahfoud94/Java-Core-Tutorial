package DataTypes;

public class DataTypes {
    public static void main(String[] args) {
        /////////// Integer
        // 1 byte
        byte num1 = 8;
        // 2 bytes
        short num2 = 16;
        // 4 bytes
        int num3 = 32;
        // 8 bytes
        long num4 = 64l;
        /////////// Decimal 
        // 4 bytes and decimal
        float num5 = 6.1f;
        // 8 bytes and decimal (more precition)
        double num6 = 6.55555;
        /////////// charaters 
        // 2 bytes / UnICODE not ASCII / single quote
        char c = 'k';
        /////////// boolean 
        // True or False
        boolean b = true; 
    }
}
